"""DSFB-RF GNU Radio Sink Block — Python wrapper around the Rust DsfbSinkB200.

This module is a Phase I deliverable for the gr-dsfb OOT module.

Architecture
------------
The block is a pure sink (no output ports). It accepts CF32 samples from the
channel filter output, runs the DSFB structural-anomaly engine on each batch,
and emits SigMF-formatted episode JSON on a ZeroMQ PUSH socket.

Non-Intrusion Guarantee
-----------------------
- The block has no output port connected to any upstream GNU Radio block.
- It does not modify USRP registers, AGC state, filter taps, or detector
  thresholds.
- If disconnected or crashed, the upstream flowgraph continues identically.

Rust Library Loading
--------------------
The Rust `dsfb-rf` crate is loaded via ctypes.  Build it with:

    cd <repo>/dsfb-rf && cargo build --release --features std

The C-ABI surface is declared in `dsfb_rf.h` (generated by cbindgen).
If the Rust library is not found, the block falls back to a pure-Python
reference implementation (slower, same algorithm).

C ABI
-----
The Rust crate exposes the following C functions (cbindgen-generated header):

    dsfb_sink_t* dsfb_sink_new(float rho_init, float tau);
    void         dsfb_sink_free(dsfb_sink_t* sink);
    uint8_t      dsfb_sink_process(dsfb_sink_t* sink,
                                   const float* samples,
                                   uintptr_t   n_samples,
                                   float       snr_db);
    uint32_t     dsfb_sink_episodes(dsfb_sink_t* sink);
    float        dsfb_sink_rho(dsfb_sink_t* sink);
    uint8_t      dsfb_sink_phase(dsfb_sink_t* sink);  // 0=Calibrating, 1=Operational

    // PolicyDecision: 0=Silent, 1=Watch, 2=Review, 3=Escalate

ZeroMQ Episode Format
---------------------
Each Review/Escalate episode is pushed as a JSON object:

    {
      "core:sample_start":       <int>,
      "core:sample_count":       <int | null>,
      "core:label":              "<grammar_state_string>",
      "dsfb:motif":              "<motif_name>",
      "dsfb:dsa_score":          <float>,
      "dsfb:lyapunov_lambda":    <float>,
      "dsfb:policy":             "Review" | "Escalate",
      "dsfb:platform_tag":       "<string>"
    }
"""

